/**
 * Options for `generateBindings`. Currently used to thread per-program
 * dynamic-record helper configuration through to the emitter; plugin-leo
 * filters the global helper map by ownership before passing here.
 */
export interface GenerateBindingsOptions {
  readonly dynamicRecords?: readonly ResolvedDynamicRecordHelperEmit[];
}

/**
 * Resolved helper entry as the emitter consumes it. `sourceProgram` is bound
 * to the owning programId by the time the emitter runs (plugin-leo's routing
 * step). Shape-equivalent to `ResolvedDynamicRecordHelper` from `@lionden/config`
 * but redeclared locally so the codegen layer doesn't depend on the config
 * package.
 */
export interface ResolvedDynamicRecordHelperEmit {
  readonly helperName: string;
  readonly sourceRecord: string;
  readonly sourceProgram: string;
  readonly schema: Readonly<Record<string, string>>;
}

