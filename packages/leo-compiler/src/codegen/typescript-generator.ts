/**
 * Generate TypeScript bindings from a ProgramABI.
 * Produces a single .ts file with:
 * - Struct/record interfaces
 * - Storage variable interface
 * - Serializer/deserializer functions
 * - Typed contract wrapper class
 */
export function generateBindings(
  abi: ProgramABI,
  allAbis: readonly ProgramABI[] = [abi],
  options: GenerateBindingsOptions = {},
): string {
  const lines: string[] = [];

  const helpers = (options.dynamicRecords ?? []).filter(
    (helper) => helper.sourceProgram === abi.program,
  );
  const includeLeoValueImport = helpers.length > 0;
  const className = resolveContractClassName(abi, { includeLeoValueImport });

  // Helper names are module-level `export const`s; reserve them so derived
  // external aliases/value bindings (e.g. `GoldToken_Token`) bump away from a
  // helper that happens to share that name, instead of redeclaring it (TS2451).
  const ctx = createGenerationContext(
    abi,
    allAbis,
    helpers.map((helper) => helper.helperName),
    includeLeoValueImport,
  );
  assertCodegenSupportedTypes(abi);
  assertNoExecutableConstParameters(abi);
  assertNoReservedLocalTypeNames(abi);
  assertNoReservedContractMembers(abi);
  assertNoReservedHelperNames(
    abi,
    className,
    includeLeoValueImport,
    helpers,
    ctx.externalInfoByKey.values(),
  );

  const hasExternalRefs = ctx.externalStructRefs.size + ctx.externalRecordRefs.size > 0;

  lines.push("// Auto-generated by @lionden/leo-compiler — do not edit");
  lines.push(`// Program: ${abi.program}`);
  lines.push("");
  lines.push(
    baseContractImport(includeLeoValueImport, hasExternalRefs ? ctx.widenInputAlias : null),
  );
  lines.push(...generateExternalImports(ctx));
  lines.push("");

  for (const struct of abi.structs) {
    lines.push(...generateStructInterface(struct, ctx));
    lines.push("");
    lines.push(...generateStructInputInterface(struct, ctx));
    lines.push("");
  }

  for (const record of abi.records) {
    lines.push(...generateRecordInterface(record, ctx));
    lines.push("");
    lines.push(...generateRecordInputInterface(record, ctx));
    lines.push("");
  }

  if (abi.storage_variables.length > 0) {
    lines.push(...generateStorageInterface(className, abi.storage_variables, ctx));
    lines.push("");
  }

  if (abi.structs.length > 0 || abi.records.length > 0) {
    lines.push("// ---------------------------------------------------------------------------");
    lines.push("// Serialization / deserialization helpers");
    lines.push("// ---------------------------------------------------------------------------");
    lines.push("");

    for (const struct of abi.structs) {
      lines.push(...generateStructSerializer(struct, ctx));
      lines.push("");
      lines.push(...generateStructDeserializer(struct, ctx));
      lines.push("");
    }

    for (const record of abi.records) {
      lines.push(...generateRecordSerializer(record, ctx));
      lines.push("");
      lines.push(...generateRecordDeserializer(record, ctx));
      lines.push("");
      lines.push(...generateRecordDecryptor(record));
      lines.push("");
    }
  }

  if (helpers.length > 0) {
    lines.push("// ---------------------------------------------------------------------------");
    lines.push("// Interface conversion helpers");
    lines.push("// ---------------------------------------------------------------------------");
    lines.push("");
    for (const helper of helpers) {
      lines.push(...generateDynamicRecordHelper(helper, abi, ctx));
      lines.push("");
    }
  }

  lines.push("// ---------------------------------------------------------------------------");
  lines.push(`// ${className} contract wrapper`);
  lines.push("// ---------------------------------------------------------------------------");
  lines.push("");
  lines.push(...generateContractClass(abi, className, ctx));

  return lines.join("\n") + "\n";
}

function assertNoExecutableConstParameters(abi: ProgramABI): void {
  for (const transition of abi.transitions) {
    if (transition.const_parameters && transition.const_parameters.length > 0) {
      throw new CodegenError(
        `Code generation for ${abi.program}/${transition.name} is unsupported: ` +
          "executable functions with const_parameters are not yet supported.",
        {
          programId: abi.program,
          phase: "generate",
        },
      );
    }
  }
}

function assertCodegenSupportedTypes(abi: ProgramABI): void {
  // Keep this allowlist in sync with the primitive branches that actually emit
  // bindings and serializer/parser calls below. The validation pass exists so
  // those lower-level helpers never fall through to unsafe generic codegen.
  const visitPrimitive = (prim: PrimitiveType, abiPath: string): void => {
    if (typeof prim === "string") {
      switch (prim) {
        case "Address":
        case "Boolean":
        case "Field":
        case "Group":
        case "Identifier":
        case "Scalar":
          return;
        default:
          throwUnsupportedPrimitive(abi.program, abiPath, prim);
      }
    }

    if (typeof prim === "object" && prim !== null) {
      if ("UInt" in prim) {
        switch (prim.UInt) {
          case "U8":
          case "U16":
          case "U32":
          case "U64":
          case "U128":
            return;
          default:
            throwUnsupportedPrimitive(abi.program, abiPath, `UInt(${String(prim.UInt)})`);
        }
      }
      if ("Int" in prim) {
        switch (prim.Int) {
          case "I8":
          case "I16":
          case "I32":
          case "I64":
          case "I128":
            return;
          default:
            throwUnsupportedPrimitive(abi.program, abiPath, `Int(${String(prim.Int)})`);
        }
      }
    }

    throwUnsupportedPrimitive(abi.program, abiPath, JSON.stringify(prim)!);
  };

  const visitPlaintext = (ty: PlaintextType, abiPath: string): void => {
    if ("Primitive" in ty) {
      visitPrimitive(ty.Primitive, `${abiPath}.Primitive`);
      return;
    }
    // Struct references are validated at their declaration site in the owning
    // program's generateBindings() call; unresolved external refs intentionally
    // remain dynamic/plaintext fallback surfaces.
    if ("Struct" in ty) return;
    if ("Array" in ty) {
      visitPlaintext(ty.Array[0], `${abiPath}.Array[0]`);
      return;
    }
    if ("Optional" in ty) {
      visitPlaintext(ty.Optional, `${abiPath}.Optional`);
    }
  };

  const visitAleo = (ty: AleoType, abiPath: string): void => {
    if (ty === "DynamicRecord") return;
    if ("Plaintext" in ty) {
      visitPlaintext(ty.Plaintext, `${abiPath}.Plaintext`);
      return;
    }
    // Record references follow the same declaration-site validation contract
    // as struct references.
    if ("Record" in ty) return;
    if ("Future" in ty) return;
  };

  const visitStorage = (ty: StorageType, abiPath: string): void => {
    if ("Plaintext" in ty) {
      visitPlaintext(ty.Plaintext, `${abiPath}.Plaintext`);
      return;
    }
    if ("Vector" in ty) {
      visitStorage(ty.Vector, `${abiPath}.Vector`);
    }
  };

  abi.structs.forEach((struct, structIndex) => {
    struct.fields.forEach((field, fieldIndex) => {
      visitPlaintext(field.ty, `structs[${structIndex}].fields[${fieldIndex}].ty`);
    });
  });

  abi.records.forEach((record, recordIndex) => {
    record.fields.forEach((field, fieldIndex) => {
      visitPlaintext(field.ty, `records[${recordIndex}].fields[${fieldIndex}].ty`);
    });
  });

  abi.mappings.forEach((mapping, mappingIndex) => {
    visitPlaintext(mapping.key, `mappings[${mappingIndex}].key`);
    visitPlaintext(mapping.value, `mappings[${mappingIndex}].value`);
  });

  abi.storage_variables.forEach((variable, variableIndex) => {
    visitStorage(variable.ty, `storage_variables[${variableIndex}].ty`);
  });

  abi.transitions.forEach((transition, transitionIndex) => {
    transition.inputs.forEach((input, inputIndex) => {
      visitAleo(input.ty, `transitions[${transitionIndex}].inputs[${inputIndex}].ty`);
    });
    transition.outputs.forEach((output, outputIndex) => {
      visitAleo(output.ty, `transitions[${transitionIndex}].outputs[${outputIndex}].ty`);
    });
  });
}

function throwUnsupportedPrimitive(programId: string, abiPath: string, primitive: string): never {
  throw new CodegenError(
    `Code generation for program ${programId} is unsupported at ABI path ${abiPath}: ` +
      `Primitive::${primitive} is not supported by generated bindings yet.`,
    {
      programId,
      abiPath,
      primitive,
      phase: "generate",
    },
  );
}

