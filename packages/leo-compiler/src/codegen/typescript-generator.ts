/**
 * A local struct/record whose generated interface name equals a fixed
 * BaseContract import (e.g. `LeoField`, `BaseContract`) collides with that
 * import (TS2440). Interface names mirror the Leo declaration and can't be
 * silently renamed like the contract class, so fail with a clear message.
 */
function assertNoReservedLocalTypeNames(abi: ProgramABI): void {
  const reserved = new Set<string>(RESERVED_BASE_CONTRACT_TYPE_IMPORT_NAMES);
  const check = (kind: "struct" | "record", path: readonly string[]): void => {
    const name = pathToTsName(path);
    if (reserved.has(name)) {
      throw new CodegenError(
        `Program ${abi.program} declares a ${kind} '${name}', whose generated interface collides with the reserved binding '${name}' imported from BaseContract. Rename the ${kind}.`,
        { programId: abi.program, typeName: name, kind, phase: "generate" },
      );
    }
  };
  for (const struct of abi.structs) check("struct", struct.path);
  for (const record of abi.records) check("record", record.path);
}

/**
 * A transition whose generated `readonly ${name}` accessor collides with an
 * inherited BaseContract member, or with the `mappings`/`storage` container when
 * the program has mappings/storage. These can't be auto-renamed without changing
 * the public call API (`contract.${name}`), so fail with a clear message.
 */
function assertNoReservedContractMembers(abi: ProgramABI): void {
  const reserved = new Set<string>(RESERVED_CONTRACT_INSTANCE_MEMBERS);
  if (abi.mappings.length > 0) reserved.add("mappings");
  if (abi.storage_variables.length > 0) reserved.add("storage");
  for (const transition of abi.transitions) {
    if (reserved.has(transition.name)) {
      throw new CodegenError(
        `Program ${abi.program} has a transition '${transition.name}' whose generated accessor collides with a reserved contract member (inherited BaseContract members such as connect/withSigner/address/programId, or the mappings/storage container). Rename the transition.`,
        { programId: abi.program, transition: transition.name, phase: "generate" },
      );
    }
  }
}

/**
 * A dynamic-record helper is emitted as a module-level `export const
 * ${helperName} = Object.assign(_${helperName}Impl, …)`, backed by a sibling
 * `function _${helperName}Impl(…)`. Reject a helper whose name equals any other
 * module-level value binding the generator emits, since two `const`s (or a
 * `const`/`function` and an imported value) of the same name is a duplicate
 * declaration (TS2451/TS2440):
 *   - the fixed BaseContract value imports (`BaseContract`,
 *     `createRecordOutputMatcher`, and `Leo` whenever helpers are emitted),
 *   - the contract class and its `create${class}` factory,
 *   - the per-type `serialize`/`deserialize`/`decrypt` consts,
 *   - the external ref serializer/deserializer aliases and record value bindings
 *     imported/re-exported for cross-program types (e.g. `serializeGoldToken_Token`),
 *   - the `_${otherHelper}Impl` backing function of any other helper (e.g. a
 *     helper `_asReceiptImpl` clashes with helper `asReceipt`'s impl function).
 * The contract class is silently suffixed away from such collisions, but a
 * helper name is user-chosen config (`codegen.dynamicRecords`) that can't be
 * renamed without changing the call API, so fail with a clear message instead.
 */
function assertNoReservedHelperNames(
  abi: ProgramABI,
  className: string,
  includeLeoValueImport: boolean,
  helpers: readonly { readonly helperName: string }[],
  externalInfos: Iterable<ExternalRefInfo>,
): void {
  if (helpers.length === 0) return;
  // Reserved value-binding name -> short description of what it would clash with.
  const reserved = new Map<string, string>();
  for (const name of baseContractValueImportNames(includeLeoValueImport)) {
    reserved.set(name, `the '${name}' value imported from BaseContract`);
  }
  reserved.set(className, `the contract class '${className}'`);
  reserved.set(`create${className}`, `the contract factory 'create${className}'`);
  for (const struct of abi.structs) {
    const name = pathToTsName(struct.path);
    reserved.set(`serialize${name}`, `the generated 'serialize${name}'`);
    reserved.set(`deserialize${name}`, `the generated 'deserialize${name}'`);
  }
  for (const record of abi.records) {
    const name = pathToTsName(record.path);
    reserved.set(`serialize${name}`, `the generated 'serialize${name}'`);
    reserved.set(`deserialize${name}`, `the generated 'deserialize${name}'`);
    reserved.set(`decrypt${name}`, `the generated 'decrypt${name}'`);
  }
  // External refs import `serialize${Base} as ${serializerName}` /
  // `deserialize${Base} as ${deserializerName}` and (for records) re-export
  // `export const ${typeName}`. The type aliases already bump away from helper
  // names, but the serializer/deserializer value aliases do not — reserve them.
  for (const info of externalInfos) {
    reserved.set(info.serializerName, `the imported external serializer '${info.serializerName}'`);
    reserved.set(
      info.deserializerName,
      `the imported external deserializer '${info.deserializerName}'`,
    );
    reserved.set(info.typeName, `the external type binding '${info.typeName}'`);
  }
  // Each helper also emits a `function _${helperName}Impl` backing declaration.
  // `_${helperName}Impl` is never equal to its own helper's name, so this only
  // catches one helper's name colliding with another helper's impl function.
  for (const helper of helpers) {
    reserved.set(
      `_${helper.helperName}Impl`,
      `the backing function of dynamic-record helper '${helper.helperName}'`,
    );
  }
  for (const helper of helpers) {
    const clashesWith = reserved.get(helper.helperName);
    if (clashesWith !== undefined) {
      throw new CodegenError(
        `Program ${abi.program} configures a dynamic-record helper '${helper.helperName}', whose generated 'export const ${helper.helperName}' collides with ${clashesWith}. Rename the helper (codegen.dynamicRecords).`,
        { programId: abi.program, helperName: helper.helperName, phase: "generate" },
      );
    }
  }
}

interface GenerationContext {
  readonly programId: string;
  readonly programsById: ReadonlyMap<string, ProgramABI>;
  readonly structsByKey: ReadonlyMap<string, StructABI>;
  readonly recordsByKey: ReadonlyMap<string, RecordABI>;
  readonly externalStructRefs: ReadonlyMap<string, StructRef>;
  readonly externalRecordRefs: ReadonlyMap<string, RecordRef>;
  /**
   * Resolved import aliases for external struct/record refs, keyed by
   * `externalRefKey`. This is the single source of truth for external type
   * aliases and their serializer/deserializer aliases, so emission does not
   * recompute `${ProgramClass}_${TypeName}` at each call site.
   */
  readonly externalInfoByKey: ReadonlyMap<string, ExternalRefInfo>;
  /**
   * Resolved `${Name}Input` identifier for every local struct/record (keyed by
   * `pathKey`) and external struct/record ref (keyed by `externalRefKey`). The
   * single source of truth for input-type names — interface/alias emitters,
   * input binding routers, serializer signatures, and cast sites all read from
   * here rather than recomputing `${name}Input` by concatenation.
   */
  readonly inputNameByKey: ReadonlyMap<string, string>;
  /**
   * Local name under which `WidenInput` is imported from `BaseContract` (used by
   * external input aliases). Normally `"WidenInput"`, but mangled deterministically
   * (`WidenInput_`, …) if a generated output type/class is named `WidenInput`, so
   * the import never conflicts with a local declaration (TS2440).
   */
  readonly widenInputAlias: string;
}

function createGenerationContext(
  abi: ProgramABI,
  allAbis: readonly ProgramABI[],
  reservedHelperNames: readonly string[] = [],
  includeLeoValueImport = false,
): GenerationContext {
  const programsById = new Map<string, ProgramABI>();
  for (const programAbi of allAbis) {
    programsById.set(programAbi.program, programAbi);
  }
  programsById.set(abi.program, abi);

  const externalStructRefs = new Map<string, StructRef>();
  const externalRecordRefs = new Map<string, RecordRef>();
  collectExternalRefs(abi, programsById, externalStructRefs, externalRecordRefs);

  const externalInfoByKey = resolveExternalInfos(
    abi,
    externalStructRefs,
    externalRecordRefs,
    reservedHelperNames,
    includeLeoValueImport,
  );
  const { inputNameByKey, widenInputAlias } = resolveInputNames(
    abi,
    externalStructRefs,
    externalRecordRefs,
    externalInfoByKey,
    reservedHelperNames,
    includeLeoValueImport,
  );

  return {
    programId: abi.program,
    programsById,
    structsByKey: new Map(abi.structs.map((struct) => [pathKey(struct.path), struct])),
    recordsByKey: new Map(abi.records.map((record) => [pathKey(record.path), record])),
    externalStructRefs,
    externalRecordRefs,
    externalInfoByKey,
    inputNameByKey,
    widenInputAlias,
  };
}

/**
 * Names this module emits as top-level declarations from the program's own ABI:
 * the contract class, its `${class}Storage` interface, and every local
 * struct/record interface. Shared by the external-alias and input-name
 * disambiguation passes so they reserve an identical base set. `${class}Storage`
 * is reserved even when the program has no storage variables — harmless
 * over-reservation that only ever yields more-unique names.
 */
function localDeclaredNames(abi: ProgramABI, includeLeoValueImport: boolean): string[] {
  const className = resolveContractClassName(abi, { includeLeoValueImport });
  return [
    className,
    `${className}Storage`,
    ...abi.structs.map((s) => pathToTsName(s.path)),
    ...abi.records.map((r) => pathToTsName(r.path)),
  ];
}

/**
 * Stable ordering for external refs by their `externalRefKey`, so underscore
 * disambiguation is deterministic across runs.
 */
function sortRefsByKey<T extends StructRef | RecordRef>(refs: Iterable<T>): T[] {
  return [...refs].sort((a, b) => externalRefKey(a).localeCompare(externalRefKey(b)));
}

/**
 * Build deterministic external type/helper aliases before emission so imports,
 * type annotations, serializers, and deserializers all use the same resolved
 * names. The preferred alias is `${ProgramClass}_${TypeName}`; underscores are
 * appended only when needed to avoid a local declaration in this module.
 */
function resolveExternalInfos(
  abi: ProgramABI,
  externalStructRefs: ReadonlyMap<string, StructRef>,
  externalRecordRefs: ReadonlyMap<string, RecordRef>,
  reservedHelperNames: readonly string[] = [],
  includeLeoValueImport = false,
): Map<string, ExternalRefInfo> {
  const reserved = new Set<string>([
    ...localDeclaredNames(abi, includeLeoValueImport),
    ...reservedHelperNames,
  ]);
  // Structs and records share one map keyed by `externalRefKey` (program+path).
  // Leo keeps structs and records in a single type namespace, so no struct and
  // record can share a program+path — entries never alias each other here.
  const infoByKey = new Map<string, ExternalRefInfo>();

  const claim = (ref: StructRef | RecordRef, baseName: string): void => {
    const programId = ref.program!;
    const programClass = programIdToClassName(programId);
    const preferred = `${programClass}_${baseName}`;
    let typeName = preferred;
    let underscores = 1;
    while (reserved.has(typeName)) {
      typeName = `${preferred}${"_".repeat(underscores)}`;
      underscores++;
    }
    reserved.add(typeName);
    infoByKey.set(externalRefKey(ref), {
      programId,
      className: programClass,
      baseName,
      typeName,
      serializerName: `serialize${typeName}`,
      deserializerName: `deserialize${typeName}`,
    });
  };

  for (const ref of sortRefsByKey(externalStructRefs.values())) claim(ref, structRefName(ref));
  for (const ref of sortRefsByKey(externalRecordRefs.values())) claim(ref, recordRefName(ref));

  return infoByKey;
}

/**
 * Identifiers imported from `BaseContract` that end in `Input` (the only fixed
 * imports a `${name}Input` alias could collide with).
 */
const RESERVED_INPUT_IMPORTS: readonly string[] = [
  "AddressInput",
  "FieldInput",
  "GroupInput",
  "ScalarInput",
  "IdentifierInput",
  "DynamicRecordInput",
  "PlaintextInput",
  "WidenInput",
];

/**
 * Build the deterministic `${Name}Input` map for all local and external
 * struct/record types. Runs before any emission; output is a pure function of
 * the ABI set + the fixed import list, so it is reproducible across runs.
 */
function resolveInputNames(
  abi: ProgramABI,
  externalStructRefs: ReadonlyMap<string, StructRef>,
  externalRecordRefs: ReadonlyMap<string, RecordRef>,
  externalInfoByKey: ReadonlyMap<string, ExternalRefInfo>,
  reservedHelperNames: readonly string[] = [],
  includeLeoValueImport = false,
): { inputNameByKey: Map<string, string>; widenInputAlias: string } {
  // `declared` = names this module emits as top-level declarations (interfaces,
  // class, storage, external re-exports, dynamic-record helper consts, and the
  // input aliases claimed below). Kept distinct from the fixed imports so the
  // WidenInput import alias can be checked against genuine declaration
  // collisions only.
  const declared = new Set<string>([
    ...localDeclaredNames(abi, includeLeoValueImport),
    ...reservedHelperNames,
  ]);
  for (const ref of externalStructRefs.values()) {
    declared.add(externalInfoByKey.get(externalRefKey(ref))!.typeName);
  }
  for (const ref of externalRecordRefs.values()) {
    declared.add(externalInfoByKey.get(externalRefKey(ref))!.typeName);
  }
  const reserved = new Set<string>([...RESERVED_INPUT_IMPORTS, ...declared]);

  const inputNameByKey = new Map<string, string>();
  const claim = (key: string, base: string): void => {
    const name = disambiguateInputName(base, reserved);
    reserved.add(name);
    declared.add(name);
    inputNameByKey.set(key, name);
  };

  // Deterministic order: local structs, then local records (ABI order), then
  // external structs and records sorted by their stable ref key.
  for (const struct of abi.structs) claim(pathKey(struct.path), pathToTsName(struct.path));
  for (const record of abi.records) claim(pathKey(record.path), pathToTsName(record.path));
  for (const ref of sortRefsByKey(externalStructRefs.values())) {
    claim(externalRefKey(ref), externalInfoByKey.get(externalRefKey(ref))!.typeName);
  }
  for (const ref of sortRefsByKey(externalRecordRefs.values())) {
    claim(externalRefKey(ref), externalInfoByKey.get(externalRefKey(ref))!.typeName);
  }

  // Choose a local name for the imported `WidenInput` that no generated
  // declaration shadows (a local type/class literally named `WidenInput` would
  // otherwise conflict with the import — TS2440). Checked against `declared`
  // only, not the fixed imports.
  const forbidden = declared;
  let widenInputAlias = "WidenInput";
  let underscores = 1;
  while (forbidden.has(widenInputAlias)) {
    widenInputAlias = `WidenInput${"_".repeat(underscores)}`;
    underscores++;
  }

  return { inputNameByKey, widenInputAlias };
}

/** Resolved input-type name for a struct ref (local or external). */
function structInputName(ref: StructRef, ctx: GenerationContext): string {
  const key = isLocalStructRef(ref, ctx) ? pathKey(ref.path) : externalRefKey(ref);
  return ctx.inputNameByKey.get(key) ?? `${structRefName(ref)}Input`;
}

/** Resolved input-type name for a record ref (local or external). */
function recordInputName(ref: RecordRef, ctx: GenerationContext): string {
  const key = isLocalRecordRef(ref, ctx) ? pathKey(ref.path) : externalRefKey(ref);
  return ctx.inputNameByKey.get(key) ?? `${recordRefName(ref)}Input`;
}

function collectExternalRefs(
  abi: ProgramABI,
  programsById: ReadonlyMap<string, ProgramABI>,
  structs: Map<string, StructRef>,
  records: Map<string, RecordRef>,
): void {
  const addStruct = (ref: StructRef): void => {
    if (!ref.program || ref.program === abi.program) return;
    const programAbi = programsById.get(ref.program);
    if (!programAbi?.structs.some((struct) => pathKey(struct.path) === pathKey(ref.path))) return;
    structs.set(externalRefKey(ref), ref);
  };

  const addRecord = (ref: RecordRef): void => {
    if (!ref.program || ref.program === abi.program) return;
    const programAbi = programsById.get(ref.program);
    if (!programAbi?.records.some((record) => pathKey(record.path) === pathKey(ref.path))) return;
    records.set(externalRefKey(ref), ref);
  };

  const visitPlaintext = (ty: PlaintextType): void => {
    if ("Struct" in ty) {
      addStruct(ty.Struct);
      return;
    }
    if ("Array" in ty) {
      visitPlaintext(ty.Array[0]);
      return;
    }
    if ("Optional" in ty) {
      visitPlaintext(ty.Optional);
    }
  };

  const visitAleo = (ty: AleoType): void => {
    if (ty === "DynamicRecord") return;
    if ("Plaintext" in ty) {
      visitPlaintext(ty.Plaintext);
      return;
    }
    if ("Record" in ty) {
      addRecord(ty.Record);
    }
  };

  const visitStorage = (ty: StorageType): void => {
    if ("Plaintext" in ty) {
      visitPlaintext(ty.Plaintext);
      return;
    }
    if ("Vector" in ty) {
      visitStorage(ty.Vector);
    }
  };

  for (const struct of abi.structs) {
    for (const field of struct.fields) visitPlaintext(field.ty);
  }
  for (const record of abi.records) {
    for (const field of record.fields) visitPlaintext(field.ty);
  }
  for (const mapping of abi.mappings) {
    visitPlaintext(mapping.key);
    visitPlaintext(mapping.value);
  }
  for (const variable of abi.storage_variables) visitStorage(variable.ty);
  for (const transition of abi.transitions) {
    for (const input of transition.inputs) visitAleo(input.ty);
    for (const output of transition.outputs) visitAleo(output.ty);
  }
}

