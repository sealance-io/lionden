interface ExternalRefInfo {
  readonly programId: string;
  readonly className: string;
  readonly baseName: string;
  readonly typeName: string;
  readonly serializerName: string;
  readonly deserializerName: string;
}

function generateExternalImports(ctx: GenerationContext): string[] {
  const importsByProgram = new Map<string, Set<string>>();

  for (const ref of ctx.externalStructRefs.values()) {
    const info = externalStructInfo(ref, ctx);
    if (!info) continue;
    const imports = importsByProgram.get(info.programId) ?? new Set<string>();
    imports.add(`type ${info.baseName} as ${info.typeName}`);
    imports.add(`serialize${info.baseName} as ${info.serializerName}`);
    imports.add(`deserialize${info.baseName} as ${info.deserializerName}`);
    importsByProgram.set(info.programId, imports);
  }

  // External record types: alias the imported type under a private `_`-prefixed
  // name so we can re-export the public name as both a type alias and a value
  // binding (carrying `.output`).
  const externalRecords: ExternalRefInfo[] = [];
  for (const ref of ctx.externalRecordRefs.values()) {
    const info = externalRecordInfo(ref, ctx);
    if (!info) continue;
    externalRecords.push(info);
    const imports = importsByProgram.get(info.programId) ?? new Set<string>();
    imports.add(`type ${info.baseName} as _${info.typeName}`);
    imports.add(`serialize${info.baseName} as ${info.serializerName}`);
    imports.add(`deserialize${info.baseName} as ${info.deserializerName}`);
    importsByProgram.set(info.programId, imports);
  }

  const importLines = [...importsByProgram.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([programId, imports]) =>
      `import { ${[...imports].sort().join(", ")} } from "./${programIdToClassName(programId)}.js";`,
    );

  // Re-export each external record's type alias publicly, and emit a same-named
  // value binding carrying the `.output` matcher. The merged type + value
  // pair lets callers write both `IdOnlyExternalRecordHandle<GoldToken_Token>`
  // (type position) and `GoldToken_Token.output` (value position).
  const valueLines: string[] = [];
  for (const info of externalRecords.sort((a, b) => a.typeName.localeCompare(b.typeName))) {
    valueLines.push(`export type ${info.typeName} = _${info.typeName};`);
    valueLines.push(`export const ${info.typeName} = {`);
    valueLines.push(`  output: createRecordOutputMatcher<_${info.typeName}>({`);
    valueLines.push(`    program: "${info.programId}",`);
    valueLines.push(`    recordName: "${info.baseName}",`);
    valueLines.push(`    deserialize: ${info.deserializerName},`);
    valueLines.push(`  }),`);
    valueLines.push("};");
  }

  // Widened input aliases for external struct/record refs. Local types get
  // explicit `${Name}Input` interfaces; external refs reuse the imported branded
  // type through `WidenInput<…>` so no cross-program `Input` import is needed.
  // Emitted for both struct and record refs (so struct-only programs still get
  // them — hence no early return before this block).
  const aliases: { readonly alias: string; readonly typeName: string }[] = [];
  for (const ref of ctx.externalStructRefs.values()) {
    const info = externalStructInfo(ref, ctx);
    if (info) aliases.push({ alias: structInputName(ref, ctx), typeName: info.typeName });
  }
  for (const ref of ctx.externalRecordRefs.values()) {
    const info = externalRecordInfo(ref, ctx);
    if (info) aliases.push({ alias: recordInputName(ref, ctx), typeName: info.typeName });
  }
  const aliasLines = aliases
    .sort((a, b) => a.alias.localeCompare(b.alias))
    .map(({ alias, typeName }) => `export type ${alias} = ${ctx.widenInputAlias}<${typeName}>;`);

  const tail: string[] = [];
  if (valueLines.length > 0) tail.push("", ...valueLines);
  if (aliasLines.length > 0) tail.push("", ...aliasLines);
  return [...importLines, ...tail];
}

function isLocalStructRef(ref: StructRef, ctx: GenerationContext): boolean {
  if (ref.program !== null && ref.program !== ctx.programId) return false;
  return ctx.structsByKey.has(pathKey(ref.path));
}

function isLocalRecordRef(ref: RecordRef, ctx: GenerationContext): boolean {
  if (ref.program !== null && ref.program !== ctx.programId) return false;
  return ctx.recordsByKey.has(pathKey(ref.path));
}

function externalStructInfo(ref: StructRef, ctx: GenerationContext): ExternalRefInfo | null {
  const programId = ref.program;
  if (!programId || programId === ctx.programId) return null;

  const abi = ctx.programsById.get(programId);
  if (!abi?.structs.some((struct) => pathKey(struct.path) === pathKey(ref.path))) return null;

  return externalInfo(programId, structRefName(ref));
}

function externalRecordInfo(ref: RecordRef, ctx: GenerationContext): ExternalRefInfo | null {
  const programId = ref.program;
  if (!programId || programId === ctx.programId) return null;

  const abi = ctx.programsById.get(programId);
  if (!abi?.records.some((record) => pathKey(record.path) === pathKey(ref.path))) return null;

  return externalInfo(programId, recordRefName(ref));
}

function externalInfo(programId: string, baseName: string): ExternalRefInfo {
  const className = programIdToClassName(programId);
  const typeName = `${className}_${baseName}`;
  return {
    programId,
    className,
    baseName,
    typeName,
    serializerName: `serialize${typeName}`,
    deserializerName: `deserialize${typeName}`,
  };
}

function pathKey(path: readonly string[]): string {
  return path.join("::");
}

function externalRefKey(ref: StructRef | RecordRef): string {
  return `${ref.program ?? ""}:${pathKey(ref.path)}`;
}

function formatArgsObjectType(inputs: readonly AbiInput[], ctx: GenerationContext): string {
  const fields = inputs.map((input) =>
    `readonly ${input.name}: ${aleoTypeToInputBindingTs(input.ty, ctx)}`,
  );
  return `{ ${fields.join("; ")} }`;
}

function formatTransitionMethodParams(
  hasInputs: boolean,
  argsType: string,
  optionsType: "LocalExecutionOptions" | "OnChainExecutionOptions",
): string {
  return hasInputs ? `args: ${argsType}, options?: ${optionsType}` : `options?: ${optionsType}`;
}

function formatReturnType(outputs: readonly AbiOutput[], ctx: GenerationContext): string {
  if (outputs.length === 0) return "void";
  if (outputs.length === 1) return aleoTypeToOutputBindingTs(outputs[0]!.ty, ctx);
  return `[${outputs.map((o) => aleoTypeToOutputBindingTs(o.ty, ctx)).join(", ")}]`;
}

/**
 * Typed `outputs` field shape for on-chain results. Mirrors `formatReturnType`'s
 * arity rule, with two substitutions:
 *   - Record types (local + imported) and DynamicRecord → `EncryptedRecord<T>`.
 *   - Private / default-private plaintext outputs (Aleo emits these as value
 *     ciphertexts on chain) → `EncryptedValue<T>`.
 * Public plaintext outputs keep their decoded type.
 * Future entries are dropped from the result shape (they still occupy
 * positions in rawOutputs).
 */
function formatTypedOutputType(outputs: readonly AbiOutput[], ctx: GenerationContext): string {
  const visible = outputs.filter((o) => !isFutureOutput(o.ty));
  if (visible.length === 0) return "void";
  if (visible.length === 1) return typedOutputElementType(visible[0]!, ctx);
  return `[${visible.map((o) => typedOutputElementType(o, ctx)).join(", ")}]`;
}

function isFutureOutput(ty: AleoType): boolean {
  return typeof ty === "object" && ty !== null && "Future" in ty;
}

/**
 * A plaintext output appears as a value ciphertext on chain when its
 * visibility is not explicitly `Public`. Leo emits `mode: "None"` when no
 * visibility modifier was written (Leo's default is private). Both `"None"`
 * and `"Private"` produce `ciphertext1...` on chain.
 */
function isPrivatePlaintextOutput(output: AbiOutput): boolean {
  const ty = output.ty;
  if (typeof ty !== "object" || ty === null || !("Plaintext" in ty)) return false;
  return output.mode !== "Public";
}

function typedOutputElementType(output: AbiOutput, ctx: GenerationContext): string {
  const ty = output.ty;
  if (ty === "DynamicRecord") return "IdOnlyDynamicRecordHandle";
  if (typeof ty === "object" && ty !== null && "Record" in ty) {
    if (isLocalRecordRef(ty.Record, ctx)) {
      return `EncryptedRecord<${recordRefName(ty.Record)}>`;
    }
    const external = externalRecordInfo(ty.Record, ctx);
    return external
      ? `IdOnlyExternalRecordHandle<${external.typeName}>`
      : "IdOnlyExternalRecordHandle<LeoDynamicRecord>";
  }
  if (typeof ty === "object" && ty !== null && "Plaintext" in ty) {
    const decoded = plaintextToOutputBindingTs(ty.Plaintext, ctx);
    return isPrivatePlaintextOutput(output) ? `EncryptedValue<${decoded}>` : decoded;
  }
  return "unknown";
}

/**
 * Emit the projector function literal passed to settleTyped / expectAcceptedTyped.
 * Indexes rawOutputs by ORIGINAL ABI position (Future entries reserve their slot
 * in rawOutputs but contribute nothing to the typed result shape).
 *
 * Signature is `(rawOutputs, tpk) => TOutputs` regardless of whether `tpk` is
 * consumed. When the transition has at least one private plaintext output
 * (decoded as `EncryptedValue<T>` and requiring `tpk`), the parameter name is
 * `tpk`. Otherwise it's `_tpk` to keep `noUnusedParameters`-style checks quiet.
 */
function formatProjectorExpr(
  transition: TransitionABI,
  programId: string,
  ctx: GenerationContext,
): string {
  const visibleIndices: number[] = [];
  transition.outputs.forEach((output, idx) => {
    if (!isFutureOutput(output.ty)) visibleIndices.push(idx);
  });

  const transitionName = transition.name;
  const hasPrivatePlaintext = transition.outputs.some(isPrivatePlaintextOutput);
  const tpkParam = hasPrivatePlaintext ? "tpk" : "_tpk";
  const usesTransitions = transition.outputs.some((o) => projectorElementUsesTransitions(o, ctx));
  const transitionsParam = usesTransitions ? "transitions" : "_transitions";
  const inputCount = transition.inputs.length;

  if (visibleIndices.length === 0) {
    return `(_rawOutputs: readonly RawTransitionOutput[], ${tpkParam}: string, ${transitionsParam}: readonly ConfirmedTransitionRecord[]) => undefined as void`;
  }

  const rawAccess = (abiIndex: number): string =>
    `BaseContract.rawOutputAt(rawOutputs, "${programId}", "${transitionName}", ${abiIndex})`;

  const decodeForIndex = (abiIndex: number): string => {
    const output = transition.outputs[abiIndex]!;
    const access = rawAccess(abiIndex);
    return projectorElementExpr(access, output, programId, transitionName, abiIndex, inputCount, ctx);
  };

  if (visibleIndices.length === 1) {
    const abiIndex = visibleIndices[0]!;
    return `(rawOutputs: readonly RawTransitionOutput[], ${tpkParam}: string, ${transitionsParam}: readonly ConfirmedTransitionRecord[]) => ${decodeForIndex(abiIndex)}`;
  }

  const tupleType = formatTypedOutputType(transition.outputs, ctx);
  const elems = visibleIndices.map(decodeForIndex);
  return `(rawOutputs: readonly RawTransitionOutput[], ${tpkParam}: string, ${transitionsParam}: readonly ConfirmedTransitionRecord[]) => ([${elems.join(", ")}] as ${tupleType})`;
}

/**
 * Whether the projector element for this output references the `transitions`
 * parameter — true for `dyn record` and any external `Record` (both id-only
 * constructors close over `transitions`). Local concrete records and
 * plaintext outputs don't reference `transitions`.
 */
function projectorElementUsesTransitions(output: AbiOutput, ctx: GenerationContext): boolean {
  const ty = output.ty;
  if (ty === "DynamicRecord") return true;
  if (typeof ty === "object" && ty !== null && "Record" in ty) {
    return !isLocalRecordRef(ty.Record, ctx);
  }
  return false;
}

function projectorElementExpr(
  rawExpr: string,
  output: AbiOutput,
  programId: string,
  transitionName: string,
  abiIndex: number,
  inputCount: number,
  ctx: GenerationContext,
): string {
  const ty = output.ty;
  const idOnlyAccess = `BaseContract.idOnlyRecordOutputAt(rawOutputs, "${programId}", "${transitionName}", ${abiIndex})`;
  if (ty === "DynamicRecord") {
    return `BaseContract.makeIdOnlyDynamicRecordHandle(${idOnlyAccess}, transitions)`;
  }
  if (typeof ty === "object" && ty !== null && "Record" in ty) {
    if (isLocalRecordRef(ty.Record, ctx)) {
      return `BaseContract.makeEncryptedRecord("${programId}", "${recordRefName(ty.Record)}", ${rawExpr}, deserialize${recordRefName(ty.Record)})`;
    }
    const external = externalRecordInfo(ty.Record, ctx);
    const typeArg = external?.typeName ?? "LeoDynamicRecord";
    return `BaseContract.makeIdOnlyExternalRecordHandle<${typeArg}>(${idOnlyAccess}, transitions)`;
  }
  if (typeof ty === "object" && ty !== null && "Plaintext" in ty) {
    if (isPrivatePlaintextOutput(output)) {
      // Global index = inputs.length + outputAbiIndex. Aleo's domain
      // separation places inputs first; verified by Step 0 devnode decrypt.
      const globalIndex = inputCount + abiIndex;
      const deserializer = plaintextDeserializerFn(ty.Plaintext, ctx);
      return `BaseContract.makeEncryptedValue(${rawExpr}, tpk, "${programId}", "${transitionName}", ${globalIndex}, ${deserializer})`;
    }
    return deserializePlaintextExpr(rawExpr, ty.Plaintext, ctx);
  }
  return rawExpr;
}

function programIdToClassName(programId: string): string {
  const name = programId.replace(/\.aleo$/, "");
  return name
    .split(/[_\-.]/)
    .map(capitalize)
    .join("");
}

/** Derive a class name that does not collide with struct/record interface names. */
export function resolveContractClassName(abi: ProgramABI): string {
  const base = programIdToClassName(abi.program);
  const typeNames = new Set<string>([
    ...abi.structs.map((s) => pathToTsName(s.path)),
    ...abi.records.map((r) => pathToTsName(r.path)),
  ]);
  let candidate = base;
  while (typeNames.has(candidate)) {
    candidate += "Contract";
  }
  return candidate;
}

function capitalize(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1);
}

function isValidIdentifier(name: string): boolean {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name);
}

/**
 * snake_case -> camelCase for mapping property names. Leading underscores are
 * preserved (`_internal` -> `_internal`); only internal `_x` boundaries are camelCased.
 */
function toCamelCase(name: string): string {
  const lead = /^_+/.exec(name)?.[0] ?? "";
  const rest = name.slice(lead.length);
  const camel = rest.replace(/_+([a-zA-Z0-9])/g, (_, c: string) => c.toUpperCase());
  return lead + camel;
}

/**
 * Maps each mapping's original (Leo) name to the property key to emit on the
 * generated `mappings` object. Collision-safe: if two mappings camelCase to the
 * same key, every member of that collision group is emitted under its original
 * Leo name (quoted) so no two mappings produce a duplicate object key.
 */
function buildMappingPropKeys(mappings: readonly MappingABI[]): Map<string, string> {
  const groups = new Map<string, string[]>(); // camelName -> original names
  for (const mapping of mappings) {
    const camel = toCamelCase(mapping.name);
    const originals = groups.get(camel) ?? [];
    originals.push(mapping.name);
    groups.set(camel, originals);
  }

  const propKeys = new Map<string, string>(); // original name -> emitted key token
  for (const [camel, originals] of groups) {
    if (originals.length > 1) {
      for (const original of originals) {
        propKeys.set(original, JSON.stringify(original));
      }
    } else {
      const original = originals[0]!;
      propKeys.set(original, isValidIdentifier(camel) ? camel : JSON.stringify(camel));
    }
  }
  return propKeys;
}

function indentLines(lines: readonly string[], indent: string): string[] {
  return lines.map((line) => line === "" ? "" : `${indent}${line}`);
}
