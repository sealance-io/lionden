import type {
  ProgramABI,
  StructABI,
  RecordABI,
  TransitionABI,
  MappingABI,
  StorageVariableABI,
  StorageType,
  AbiInput,
  AbiOutput,
  AleoType,
  PlaintextType,
  PrimitiveType,
  StructRef,
  RecordRef,
} from "../abi-types.js";
import {
  primitiveToTs,
  primitiveToLeoSuffix,
  isBigIntType,
  structRefName,
  recordRefName,
  pathToTsName,
} from "./type-mapper.js";
import { CONTRACT_WRAPPER_TEMPLATE } from "./contract-wrapper.js";

/**
 * Generate TypeScript bindings from a ProgramABI.
 * Produces a single .ts file with:
 * - Struct/record interfaces
 * - Storage variable interface
 * - Serializer/deserializer functions
 * - Typed contract wrapper class
 */
export function generateBindings(abi: ProgramABI): string {
  const lines: string[] = [];
  const className = resolveClassName(abi);
  const ctx = createGenerationContext(abi);

  lines.push("// Auto-generated by @lionden/leo-compiler — do not edit");
  lines.push(`// Program: ${abi.program}`);
  lines.push("");
  lines.push(
    'import { BaseContract, type LocalCallOptions, type BroadcastOptions, type TransitionCallResult } from "./BaseContract.js";',
  );
  lines.push("");

  for (const struct of abi.structs) {
    lines.push(...generateStructInterface(struct, ctx));
    lines.push("");
  }

  for (const record of abi.records) {
    lines.push(...generateRecordInterface(record, ctx));
    lines.push("");
  }

  if (abi.storage_variables.length > 0) {
    lines.push(...generateStorageInterface(className, abi.storage_variables, ctx));
    lines.push("");
  }

  if (abi.structs.length > 0 || abi.records.length > 0) {
    lines.push("// ---------------------------------------------------------------------------");
    lines.push("// Serialization / deserialization helpers");
    lines.push("// ---------------------------------------------------------------------------");
    lines.push("");

    for (const struct of abi.structs) {
      lines.push(...generateStructSerializer(struct, ctx));
      lines.push("");
      lines.push(...generateStructDeserializer(struct, ctx));
      lines.push("");
    }

    for (const record of abi.records) {
      lines.push(...generateRecordSerializer(record, ctx));
      lines.push("");
      lines.push(...generateRecordDeserializer(record, ctx));
      lines.push("");
    }
  }

  lines.push("// ---------------------------------------------------------------------------");
  lines.push(`// ${className} contract wrapper`);
  lines.push("// ---------------------------------------------------------------------------");
  lines.push("");
  lines.push(...generateContractClass(abi, className, ctx));

  return lines.join("\n") + "\n";
}

/**
 * Generate the BaseContract file content.
 */
export function generateBaseContract(): string {
  return CONTRACT_WRAPPER_TEMPLATE;
}

interface GenerationContext {
  readonly programId: string;
  readonly structsByKey: ReadonlyMap<string, StructABI>;
  readonly recordsByKey: ReadonlyMap<string, RecordABI>;
}

function createGenerationContext(abi: ProgramABI): GenerationContext {
  return {
    programId: abi.program,
    structsByKey: new Map(abi.structs.map((struct) => [pathKey(struct.path), struct])),
    recordsByKey: new Map(abi.records.map((record) => [pathKey(record.path), record])),
  };
}

// ---------------------------------------------------------------------------
// Struct/Record/Storage interface generation
// ---------------------------------------------------------------------------

function generateStructInterface(struct: StructABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(struct.path);
  const lines: string[] = [];
  lines.push(`export interface ${name} {`);
  for (const field of struct.fields) {
    lines.push(`  readonly ${field.name}: ${plaintextToBindingTs(field.ty, ctx)};`);
  }
  lines.push("}");
  return lines;
}

function generateRecordInterface(record: RecordABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(record.path);
  const lines: string[] = [];
  lines.push(`export interface ${name} {`);
  lines.push("  readonly owner: string;");
  for (const field of record.fields) {
    if (field.name === "owner") continue;
    lines.push(`  readonly ${field.name}: ${plaintextToBindingTs(field.ty, ctx)};`);
  }
  lines.push("  readonly _nonce: string;");
  lines.push("}");
  return lines;
}

function generateStorageInterface(
  className: string,
  storageVariables: readonly StorageVariableABI[],
  ctx: GenerationContext,
): string[] {
  const lines: string[] = [];
  lines.push(`export interface ${className}Storage {`);
  for (const variable of storageVariables) {
    lines.push(`  readonly ${variable.name}: ${storageTypeToBindingTs(variable.ty, ctx)};`);
  }
  lines.push("}");
  return lines;
}

// ---------------------------------------------------------------------------
// Serialization helpers
// ---------------------------------------------------------------------------

function generateStructSerializer(struct: StructABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(struct.path);
  const lines: string[] = [];
  lines.push(`export function serialize${name}(value: ${name}): string {`);
  lines.push("  const fields: string[] = [];");

  for (const field of struct.fields) {
    const serialized = serializePlaintextExpr(`value.${field.name}`, field.ty, ctx);
    lines.push(`  fields.push("${field.name}: " + ${serialized});`);
  }

  lines.push('  return "{ " + fields.join(", ") + " }";');
  lines.push("}");
  return lines;
}

function generateRecordSerializer(record: RecordABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(record.path);
  const lines: string[] = [];
  lines.push(`export function serialize${name}(value: ${name}): string {`);
  lines.push("  const fields: string[] = [];");

  for (const field of record.fields) {
    const serialized = serializePlaintextExpr(`value.${field.name}`, field.ty, ctx);
    lines.push(`  fields.push("${field.name}: " + ${serialized});`);
  }
  lines.push('  fields.push("_nonce: " + value._nonce);');

  lines.push('  return "{ " + fields.join(", ") + " }";');
  lines.push("}");
  return lines;
}

function generateStructDeserializer(struct: StructABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(struct.path);
  const lines: string[] = [];
  lines.push(`export function deserialize${name}(value: string): ${name} {`);
  lines.push("  const _fields = BaseContract.parseStruct(value);");
  lines.push("  return {");
  for (const field of struct.fields) {
    lines.push(
      `    ${field.name}: ${deserializePlaintextExpr(`_fields["${field.name}"]!`, field.ty, ctx)},`,
    );
  }
  lines.push("  };");
  lines.push("}");
  return lines;
}

function generateRecordDeserializer(record: RecordABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(record.path);
  const lines: string[] = [];
  lines.push(`export function deserialize${name}(value: string): ${name} {`);
  lines.push("  const _fields = BaseContract.parseStruct(value);");
  lines.push("  return {");
  lines.push('    owner: BaseContract.parseString(_fields["owner"]!),');
  for (const field of record.fields) {
    if (field.name === "owner") continue;
    lines.push(
      `    ${field.name}: ${deserializePlaintextExpr(`_fields["${field.name}"]!`, field.ty, ctx)},`,
    );
  }
  lines.push('    _nonce: BaseContract.parseString(_fields["_nonce"] ?? ""),');
  lines.push("  };");
  lines.push("}");
  return lines;
}

// ---------------------------------------------------------------------------
// Contract class generation
// ---------------------------------------------------------------------------

function generateContractClass(
  abi: ProgramABI,
  className: string,
  ctx: GenerationContext,
): string[] {
  const lines: string[] = [];

  lines.push(`export class ${className} extends BaseContract {`);
  lines.push("  constructor() {");
  lines.push(`    super("${abi.program}");`);
  lines.push("  }");

  for (const transition of abi.transitions) {
    lines.push("");
    lines.push(...generateTransitionMethod(transition, ctx).map((l) => "  " + l));
  }

  for (const mapping of abi.mappings) {
    lines.push("");
    lines.push(...generateMappingAccessor(mapping, ctx).map((l) => "  " + l));
  }

  lines.push("}");
  lines.push("");
  lines.push(`export function create${className}(): ${className} {`);
  lines.push(`  return new ${className}();`);
  lines.push("}");

  return lines;
}

function generateTransitionMethod(
  transition: TransitionABI,
  ctx: GenerationContext,
): string[] {
  const lines: string[] = [];
  const inputParams = transition.inputs.map((input) => formatParam(input, ctx));
  const localParams = [...inputParams, "options?: LocalCallOptions"];
  const broadcastParams = [...inputParams, "options?: BroadcastOptions"];
  const returnType = formatReturnType(transition.outputs, ctx);

  lines.push(`async ${transition.name}(`);
  for (const param of localParams) {
    lines.push(`  ${param},`);
  }
  lines.push(`): Promise<${returnType}> {`);
  lines.push(...generateSerializedArgsLines(transition.inputs, ctx, "  "));
  lines.push(`  const _result = await this.executeLocal("${transition.name}", _args, options ?? {});`);

  if (transition.outputs.length === 1) {
    const output = transition.outputs[0]!;
    lines.push(`  return ${deserializeOutputExpr("_result.outputs[0]!", output.ty, ctx)};`);
  } else if (transition.outputs.length > 1) {
    const tupleType = formatReturnType(transition.outputs, ctx);
    const elems = transition.outputs.map((o, i) =>
      deserializeOutputExpr(`_result.outputs[${i}]!`, o.ty, ctx),
    );
    lines.push(`  const _decoded: ${tupleType} = [${elems.join(", ")}];`);
    lines.push("  return _decoded;");
  }

  lines.push("}");
  lines.push("");
  lines.push(`async ${transition.name}Broadcast(`);
  for (const param of broadcastParams) {
    lines.push(`  ${param},`);
  }
  lines.push("): Promise<TransitionCallResult> {");
  lines.push(...generateSerializedArgsLines(transition.inputs, ctx, "  "));
  lines.push(`  return this.broadcast("${transition.name}", _args, options ?? {});`);
  lines.push("}");

  return lines;
}

function generateSerializedArgsLines(
  inputs: readonly AbiInput[],
  ctx: GenerationContext,
  indent: string,
): string[] {
  const lines = [`${indent}const _args: string[] = [`];
  for (const input of inputs) {
    lines.push(`${indent}  ${serializeInputExpr(input, ctx)},`);
  }
  lines.push(`${indent}];`);
  return lines;
}

function generateMappingAccessor(
  mapping: MappingABI,
  ctx: GenerationContext,
): string[] {
  const keyType = plaintextToBindingTs(mapping.key, ctx);
  const valueType = plaintextToBindingTs(mapping.value, ctx);
  const keySerializer = serializePlaintextExpr("key", mapping.key, ctx);
  const valueDeserializer = deserializePlaintextExpr("_result", mapping.value, ctx);

  return [
    `async get${capitalize(mapping.name)}(key: ${keyType}): Promise<${valueType} | null> {`,
    `  const _result = await this.queryMapping("${mapping.name}", ${keySerializer});`,
    "  if (_result === null) return null;",
    `  return ${valueDeserializer};`,
    "}",
  ];
}

// ---------------------------------------------------------------------------
// Type mapping helpers used by code generation
// ---------------------------------------------------------------------------

function plaintextToBindingTs(pt: PlaintextType, ctx: GenerationContext): string {
  if ("Primitive" in pt) return primitiveToTs(pt.Primitive);
  if ("Struct" in pt) {
    return isLocalStructRef(pt.Struct, ctx) ? structRefName(pt.Struct) : "string";
  }
  if ("Array" in pt) {
    const [elemType] = pt.Array;
    return `${plaintextToBindingTs(elemType, ctx)}[]`;
  }
  if ("Optional" in pt) return `${plaintextToBindingTs(pt.Optional, ctx)} | null`;
  return "unknown";
}

function aleoTypeToBindingTs(ty: AleoType, ctx: GenerationContext): string {
  if (ty === "DynamicRecord") return "string";
  if ("Plaintext" in ty) return plaintextToBindingTs(ty.Plaintext, ctx);
  if ("Record" in ty) {
    return isLocalRecordRef(ty.Record, ctx) ? recordRefName(ty.Record) : "string";
  }
  if ("Future" in ty) return "void";
  return "unknown";
}

function storageTypeToBindingTs(ty: StorageType, ctx: GenerationContext): string {
  if ("Plaintext" in ty) return plaintextToBindingTs(ty.Plaintext, ctx);
  if ("Vector" in ty) return `${storageTypeToBindingTs(ty.Vector, ctx)}[]`;
  return "unknown";
}

// ---------------------------------------------------------------------------
// Serialization expression generators (JS value → Leo string)
// ---------------------------------------------------------------------------

function serializeInputExpr(input: AbiInput, ctx: GenerationContext): string {
  return serializeAleoTypeExpr(input.name, input.ty, ctx);
}

function serializeAleoTypeExpr(expr: string, ty: AleoType, ctx: GenerationContext): string {
  if (ty === "DynamicRecord") return expr;
  if ("Plaintext" in ty) return serializePlaintextExpr(expr, ty.Plaintext, ctx);
  if ("Record" in ty) {
    return isLocalRecordRef(ty.Record, ctx) ? `serialize${recordRefName(ty.Record)}(${expr})` : expr;
  }
  return `String(${expr})`;
}

function serializePlaintextExpr(expr: string, ty: PlaintextType, ctx: GenerationContext): string {
  if ("Primitive" in ty) return serializePrimitive(expr, ty.Primitive);
  if ("Struct" in ty) {
    return isLocalStructRef(ty.Struct, ctx) ? `serialize${structRefName(ty.Struct)}(${expr})` : expr;
  }
  if ("Array" in ty) {
    const [elemType] = ty.Array;
    return `"[" + ${expr}.map((e: any) => ${serializePlaintextExpr("e", elemType, ctx)}).join(", ") + "]"`;
  }
  if ("Optional" in ty) {
    const innerSer = serializePlaintextExpr(expr, ty.Optional, ctx);
    const zero = zeroLeoValue(ty.Optional, ctx);
    if (zero !== null) {
      return `(${expr} != null ? "{ is_some: true, val: " + ${innerSer} + " }" : "{ is_some: false, val: ${zero} }")`;
    }
    return `(${expr} != null ? "{ is_some: true, val: " + ${innerSer} + " }" : (() => { throw new Error("Cannot serialize None for this Optional type"); })())`;
  }
  return `String(${expr})`;
}

// ---------------------------------------------------------------------------
// Deserialization expression generators (Leo string → JS value)
// ---------------------------------------------------------------------------

function deserializeOutputExpr(expr: string, ty: AleoType, ctx: GenerationContext): string {
  if (ty === "DynamicRecord") return expr;
  if ("Plaintext" in ty) return deserializePlaintextExpr(expr, ty.Plaintext, ctx);
  if ("Record" in ty) {
    return isLocalRecordRef(ty.Record, ctx) ? `deserialize${recordRefName(ty.Record)}(${expr})` : expr;
  }
  if ("Future" in ty) return "undefined as any";
  return expr;
}

function deserializePlaintextExpr(expr: string, ty: PlaintextType, ctx: GenerationContext): string {
  if ("Primitive" in ty) return deserializePrimitiveExpr(expr, ty.Primitive);
  if ("Struct" in ty) {
    return isLocalStructRef(ty.Struct, ctx) ? `deserialize${structRefName(ty.Struct)}(${expr})` : expr;
  }
  if ("Array" in ty) {
    const [elemType] = ty.Array;
    return `BaseContract.parseArray(${expr}).map((e: string) => ${deserializePlaintextExpr("e", elemType, ctx)})`;
  }
  if ("Optional" in ty) {
    return `(() => { const _opt = BaseContract.parseStruct(${expr}); return BaseContract.parseBoolean(_opt["is_some"]!) ? ${deserializePlaintextExpr('_opt["val"]!', ty.Optional, ctx)} : null; })()`;
  }
  return expr;
}

function deserializePrimitiveExpr(expr: string, prim: PrimitiveType): string {
  if (typeof prim === "string") {
    switch (prim) {
      case "Address":
      case "Field":
      case "Group":
      case "Scalar":
        return `BaseContract.parseString(${expr})`;
      case "Boolean":
        return `BaseContract.parseBoolean(${expr})`;
    }
  }
  if ("UInt" in prim || "Int" in prim) {
    if (isBigIntType(prim)) {
      return `BaseContract.parseBigInt(${expr})`;
    }
    return `BaseContract.parseNumber(${expr})`;
  }
  return expr;
}

function serializePrimitive(expr: string, prim: PrimitiveType): string {
  if (typeof prim === "string") {
    switch (prim) {
      case "Address":
      case "Field":
      case "Group":
      case "Scalar":
        return expr;
      case "Boolean":
        return `String(${expr})`;
    }
  }

  const suffix = primitiveToLeoSuffix(prim);
  return `${expr}.toString() + "${suffix}"`;
}

// ---------------------------------------------------------------------------
// Zero-value helpers (for Optional None serialization)
// ---------------------------------------------------------------------------

function zeroLeoValue(
  ty: PlaintextType,
  ctx: GenerationContext,
  seen: ReadonlySet<string> = new Set(),
): string | null {
  if ("Primitive" in ty) {
    const prim = ty.Primitive;
    if (typeof prim === "string") {
      switch (prim) {
        case "Address":
          return "aleo1qqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqqq3ljyzc";
        case "Boolean":
          return "false";
        case "Field":
          return "0field";
        case "Group":
          return "0group";
        case "Scalar":
          return "0scalar";
      }
    }
    if ("UInt" in prim) return `0${prim.UInt.toLowerCase()}`;
    if ("Int" in prim) return `0${prim.Int.toLowerCase()}`;
  }

  if ("Array" in ty) {
    const [elemType, length] = ty.Array;
    const elemZero = zeroLeoValue(elemType, ctx, seen);
    if (elemZero === null) return null;
    return `[${Array.from({ length }, () => elemZero).join(", ")}]`;
  }

  if ("Optional" in ty) {
    const innerZero = zeroLeoValue(ty.Optional, ctx, seen);
    if (innerZero === null) return null;
    return `{ is_some: false, val: ${innerZero} }`;
  }

  if ("Struct" in ty) {
    if (!isLocalStructRef(ty.Struct, ctx)) return null;

    const key = pathKey(ty.Struct.path);
    if (seen.has(key)) return null;

    const struct = ctx.structsByKey.get(key);
    if (!struct) return null;

    const nextSeen = new Set(seen);
    nextSeen.add(key);

    const fieldValues: string[] = [];
    for (const field of struct.fields) {
      const fieldZero = zeroLeoValue(field.ty, ctx, nextSeen);
      if (fieldZero === null) return null;
      fieldValues.push(`${field.name}: ${fieldZero}`);
    }
    return `{ ${fieldValues.join(", ")} }`;
  }

  return null;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function isLocalStructRef(ref: StructRef, ctx: GenerationContext): boolean {
  if (ref.program !== null && ref.program !== ctx.programId) return false;
  return ctx.structsByKey.has(pathKey(ref.path));
}

function isLocalRecordRef(ref: RecordRef, ctx: GenerationContext): boolean {
  if (ref.program !== null && ref.program !== ctx.programId) return false;
  return ctx.recordsByKey.has(pathKey(ref.path));
}

function formatParam(input: AbiInput, ctx: GenerationContext): string {
  return `${input.name}: ${aleoTypeToBindingTs(input.ty, ctx)}`;
}

function pathKey(path: readonly string[]): string {
  return path.join("::");
}

function formatReturnType(outputs: readonly AbiOutput[], ctx: GenerationContext): string {
  if (outputs.length === 0) return "void";
  if (outputs.length === 1) return aleoTypeToBindingTs(outputs[0]!.ty, ctx);
  return `[${outputs.map((o) => aleoTypeToBindingTs(o.ty, ctx)).join(", ")}]`;
}

function programIdToClassName(programId: string): string {
  const name = programId.replace(/\.aleo$/, "");
  return name
    .split(/[_\-.]/)
    .map(capitalize)
    .join("");
}

/** Derive a class name that does not collide with struct/record interface names. */
function resolveClassName(abi: ProgramABI): string {
  const base = programIdToClassName(abi.program);
  const typeNames = new Set<string>([
    ...abi.structs.map((s) => pathToTsName(s.path)),
    ...abi.records.map((r) => pathToTsName(r.path)),
  ]);
  let candidate = base;
  while (typeNames.has(candidate)) {
    candidate += "Contract";
  }
  return candidate;
}

function capitalize(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1);
}
