function generateStructSerializer(struct: StructABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(struct.path);
  const inputName = ctx.inputNameByKey.get(pathKey(struct.path)) ?? `${name}Input`;
  const lines: string[] = [];
  lines.push(`export function serialize${name}(value: ${inputName}, context?: TransitionInputContext): string {`);
  lines.push("  BaseContract.assertObject(value, context);");
  lines.push("  const fields: string[] = [];");

  for (const field of struct.fields) {
    const serialized = serializePlaintextExpr(
      `value.${field.name}`,
      field.ty,
      ctx,
      `BaseContract.childInputContext(context, "${field.name}")`,
    );
    lines.push(`  fields.push("${field.name}: " + ${serialized});`);
  }

  lines.push('  return "{ " + fields.join(", ") + " }";');
  lines.push("}");
  return lines;
}

function generateRecordSerializer(record: RecordABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(record.path);
  const inputName = ctx.inputNameByKey.get(pathKey(record.path)) ?? `${name}Input`;
  const lines: string[] = [];
  lines.push(`export function serialize${name}(value: ${inputName}, context?: TransitionInputContext): string {`);
  lines.push("  BaseContract.assertObject(value, context);");
  // Round-trip a record returned by a previous transition by replaying the
  // original literal verbatim. Without this, per-field visibility suffixes
  // mismatch on re-serialization and the runtime rejects the input. See
  // BaseContract.RECORD_RAW.
  lines.push("  const _raw = (value as unknown as { readonly [k: symbol]: unknown })[BaseContract.RECORD_RAW];");
  lines.push("  if (typeof _raw === \"string\") return _raw;");
  lines.push("  const fields: string[] = [];");

  for (const field of record.fields) {
    const serialized = serializePlaintextExpr(
      `value.${field.name}`,
      field.ty,
      ctx,
      `BaseContract.childInputContext(context, "${field.name}")`,
    );
    lines.push(`  fields.push("${field.name}: " + ${serialized});`);
  }
  lines.push('  fields.push("_nonce: " + BaseContract.serializeGroup(value._nonce, BaseContract.childInputContext(context, "_nonce")));');

  lines.push('  return "{ " + fields.join(", ") + " }";');
  lines.push("}");
  return lines;
}

function generateStructDeserializer(struct: StructABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(struct.path);
  const lines: string[] = [];
  lines.push(`export function deserialize${name}(value: string): ${name} {`);
  lines.push("  const _fields = BaseContract.parseStruct(value);");
  lines.push("  return {");
  for (const field of struct.fields) {
    lines.push(
      `    ${field.name}: ${deserializePlaintextExpr(`_fields["${field.name}"]!`, field.ty, ctx)},`,
    );
  }
  lines.push("  };");
  lines.push("}");
  return lines;
}

function generateRecordDecryptor(record: RecordABI): string[] {
  const name = pathToTsName(record.path);
  return [
    `export async function decrypt${name}(ciphertext: string, key: RecordDecryptionKey): Promise<${name}> {`,
    `  return BaseContract.decryptRecord(ciphertext, key, deserialize${name});`,
    "}",
  ];
}

/**
 * Emit a free function that converts a generated record value into a typed
 * `LeoDynamicRecord` literal using `Leo.dynamicRecord(value, schema)`. The
 * schema is fixed at codegen time from `helper.schema`; the generated
 * function takes only the record value.
 *
 * Validates the helper config against the source record's generated shape:
 * - implicit `owner: address` field
 * - each ABI record field (excluding any explicit `owner` re-declaration)
 * - implicit `_nonce: group` field
 *
 * Mismatches and unsupported primitive types throw `CodegenError` so users
 * get a precise build-time failure instead of a runtime parser error.
 */
function generateDynamicRecordHelper(
  helper: ResolvedDynamicRecordHelperEmit,
  abi: ProgramABI,
  ctx: GenerationContext,
): string[] {
  const record = abi.records.find((r) => pathToTsName(r.path) === helper.sourceRecord);
  if (!record) {
    throw new CodegenError(
      `codegen.dynamicRecords.${helper.helperName}: sourceRecord '${helper.sourceRecord}' is not a local record in '${abi.program}'.`,
      { helperName: helper.helperName, sourceRecord: helper.sourceRecord, program: abi.program },
    );
  }

  const expectedFields = buildExpectedFieldsForHelper(helper, record);

  const schemaKeys = Object.keys(helper.schema);
  const missing = [...expectedFields.keys()].filter((k) => !Object.hasOwn(helper.schema, k));
  const extra = schemaKeys.filter((k) => !expectedFields.has(k));
  if (missing.length > 0 || extra.length > 0) {
    throw new CodegenError(
      `codegen.dynamicRecords.${helper.helperName}: schema keys do not match record '${helper.sourceRecord}'. Missing: [${missing.join(", ")}]; Extra: [${extra.join(", ")}].`,
      {
        helperName: helper.helperName,
        sourceRecord: helper.sourceRecord,
        missing,
        extra,
      },
    );
  }

  for (const fieldName of schemaKeys) {
    const entry = helper.schema[fieldName]!;
    const dot = entry.lastIndexOf(".");
    if (dot < 0) {
      throw new CodegenError(
        `codegen.dynamicRecords.${helper.helperName}.schema.${fieldName}: '${entry}' is not a valid '<type>.<visibility>' schema entry.`,
        { helperName: helper.helperName, field: fieldName, schemaEntry: entry },
      );
    }
    const schemaPrim = entry.slice(0, dot);
    const expectedPrim = expectedFields.get(fieldName)!;
    if (schemaPrim !== expectedPrim) {
      throw new CodegenError(
        `codegen.dynamicRecords.${helper.helperName}.schema.${fieldName}: field has Leo type '${expectedPrim}' on record '${helper.sourceRecord}', but schema says '${schemaPrim}'.`,
        {
          helperName: helper.helperName,
          field: fieldName,
          expected: expectedPrim,
          actual: schemaPrim,
        },
      );
    }
  }

  const lines: string[] = [];
  const fnName = `_${helper.helperName}Impl`;
  // `value` is widened to the record's resolved input interface so callers may
  // pass raw field values; `.output` (below) stays the branded record type.
  const inputName = ctx.inputNameByKey.get(pathKey(record.path)) ?? `${helper.sourceRecord}Input`;
  lines.push(`function ${fnName}(value: ${inputName}): LeoDynamicRecord {`);
  lines.push("  return Leo.dynamicRecord(value, {");
  for (const [key, entry] of Object.entries(helper.schema)) {
    lines.push(`    ${key}: ${JSON.stringify(entry)} as const,`);
  }
  lines.push("  } as const);");
  lines.push("}");
  // Callable + namespace pattern: the helper is also a namespace carrying
  // `output`, the output-side matcher. Used by callers refining record
  // decryption via an id-only handle's `.match(matcher.from(...)|.at(...))`
  // or the encrypted-record arm's `.match(matcher)`.
  lines.push(`export const ${helper.helperName} = Object.assign(${fnName}, {`);
  lines.push(`  output: createRecordOutputMatcher<${helper.sourceRecord}>({`);
  lines.push(`    program: "${abi.program}",`);
  lines.push(`    recordName: "${helper.sourceRecord}",`);
  lines.push(`    deserialize: deserialize${helper.sourceRecord},`);
  lines.push(`  }),`);
  lines.push("});");
  return lines;
}

/**
 * Build the expected `<fieldName, primitive>` map matching the generated
 * record interface shape:
 *   1. implicit `owner: address`
 *   2. each ABI field that isn't a re-declaration of `owner`
 *   3. implicit `_nonce: group`
 *
 * Rejects unsupported ABI primitives (Identifier, Signature) and non-primitive
 * fields (struct, array, optional). These cases cannot be encoded by
 * `Leo.dynamicRecord` even though they may appear in a record's serialized form.
 */
function buildExpectedFieldsForHelper(
  helper: ResolvedDynamicRecordHelperEmit,
  record: RecordABI,
): Map<string, string> {
  const expectedFields = new Map<string, string>();
  expectedFields.set("owner", "address");
  for (const field of record.fields) {
    if (field.name === "owner") continue;
    const prim = primitiveOfPlaintext(field.ty);
    if (prim === null) {
      throw new CodegenError(
        `codegen.dynamicRecords.${helper.helperName}: record '${helper.sourceRecord}' contains non-primitive field '${field.name}'. Interface conversion helpers only support primitive record fields.`,
        { helperName: helper.helperName, sourceRecord: helper.sourceRecord, field: field.name },
      );
    }
    if (!SUPPORTED_DYNAMIC_RECORD_PRIMITIVES.has(prim)) {
      throw new CodegenError(
        `codegen.dynamicRecords.${helper.helperName}: record '${helper.sourceRecord}' field '${field.name}' uses unsupported primitive '${prim}'. Leo.dynamicRecord only supports: ${[...SUPPORTED_DYNAMIC_RECORD_PRIMITIVES].join("/")}.`,
        { helperName: helper.helperName, sourceRecord: helper.sourceRecord, field: field.name, primitive: prim },
      );
    }
    expectedFields.set(field.name, prim);
  }
  expectedFields.set("_nonce", "group");
  return expectedFields;
}

/**
 * Map a PlaintextType primitive to its Leo source-level name (lowercase),
 * matching the keys of `serializeDynamicRecordField`'s dispatch table.
 * Returns null for non-primitive types (struct, array, optional).
 */
function primitiveOfPlaintext(ty: PlaintextType): string | null {
  if (typeof ty !== "object" || ty === null) return null;
  if (!("Primitive" in ty)) return null;
  const prim = ty.Primitive;
  if (typeof prim === "string") {
    switch (prim) {
      case "Address": return "address";
      case "Boolean": return "boolean";
      case "Field": return "field";
      case "Group": return "group";
      case "Scalar": return "scalar";
      case "Identifier": return "identifier";
    }
    return null;
  }
  if (typeof prim === "object" && prim !== null) {
    if ("UInt" in prim) return prim.UInt.toLowerCase();
    if ("Int" in prim) return prim.Int.toLowerCase();
  }
  return null;
}

function generateRecordDeserializer(record: RecordABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(record.path);
  const lines: string[] = [];
  lines.push(`export function deserialize${name}(value: string): ${name} {`);
  lines.push("  const _fields = BaseContract.parseStruct(value);");
  lines.push(`  const _record: ${name} = {`);
  lines.push('    owner: BaseContract.parseAddress(_fields["owner"]!),');
  for (const field of record.fields) {
    if (field.name === "owner") continue;
    lines.push(
      `    ${field.name}: ${deserializePlaintextExpr(`_fields["${field.name}"]!`, field.ty, ctx)},`,
    );
  }
  lines.push('    _nonce: BaseContract.parseGroup(_fields["_nonce"] ?? ""),');
  lines.push("  };");
  // Stash the original literal so serialize${name} can replay it verbatim
  // and avoid mangling per-field visibility suffixes on round-trip.
  lines.push(
    "  Object.defineProperty(_record, BaseContract.RECORD_RAW, { value, enumerable: false });",
  );
  lines.push("  return _record;");
  lines.push("}");
  return lines;
}

