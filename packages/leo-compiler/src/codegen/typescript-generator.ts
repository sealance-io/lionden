function generateStructInterface(struct: StructABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(struct.path);
  const lines: string[] = [];
  lines.push(`export interface ${name} {`);
  for (const field of struct.fields) {
    lines.push(`  readonly ${field.name}: ${plaintextToOutputBindingTs(field.ty, ctx)};`);
  }
  lines.push("}");
  return lines;
}

function generateRecordInterface(record: RecordABI, ctx: GenerationContext): string[] {
  const name = pathToTsName(record.path);
  const lines: string[] = [];
  lines.push(`export interface ${name} {`);
  lines.push("  readonly owner: LeoAddress;");
  for (const field of record.fields) {
    if (field.name === "owner") continue;
    lines.push(`  readonly ${field.name}: ${plaintextToOutputBindingTs(field.ty, ctx)};`);
  }
  lines.push("  readonly _nonce: LeoGroup;");
  lines.push("}");
  return lines;
}

/**
 * Widened input interface mirroring `generateStructInterface` but typing every
 * field via the input binding (scalar fields accept `bigint | number`, nested
 * composites widen recursively). Used for transition args, mapping keys, and
 * serializer inputs; the branded interface above is kept for outputs/reads.
 */
function generateStructInputInterface(struct: StructABI, ctx: GenerationContext): string[] {
  const name = ctx.inputNameByKey.get(pathKey(struct.path)) ?? `${pathToTsName(struct.path)}Input`;
  const lines: string[] = [];
  lines.push(`export interface ${name} {`);
  for (const field of struct.fields) {
    lines.push(`  readonly ${field.name}: ${plaintextToInputBindingTs(field.ty, ctx)};`);
  }
  lines.push("}");
  return lines;
}

/** Widened input interface for a record (`owner`/`_nonce` widened to their *Input aliases). */
function generateRecordInputInterface(record: RecordABI, ctx: GenerationContext): string[] {
  const name = ctx.inputNameByKey.get(pathKey(record.path)) ?? `${pathToTsName(record.path)}Input`;
  const lines: string[] = [];
  lines.push(`export interface ${name} {`);
  lines.push("  readonly owner: AddressInput;");
  for (const field of record.fields) {
    if (field.name === "owner") continue;
    lines.push(`  readonly ${field.name}: ${plaintextToInputBindingTs(field.ty, ctx)};`);
  }
  lines.push("  readonly _nonce: GroupInput;");
  lines.push("}");
  return lines;
}

function generateStorageInterface(
  className: string,
  storageVariables: readonly StorageVariableABI[],
  ctx: GenerationContext,
): string[] {
  const lines: string[] = [];
  lines.push(`export interface ${className}Storage {`);
  for (const variable of storageVariables) {
    lines.push(`  readonly ${variable.name}: ${storageTypeToBindingTs(variable.ty, ctx)};`);
  }
  lines.push("}");
  return lines;
}

